const fs = require('fs');
const path = require('path');

// Create simple base64 encoded PNG icons for development
const createSimpleIcon = (size) => {
  // Simple 16x16 red square PNG in base64 (for development only)
  const pngData = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
    0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10,
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x91, 0x68, 0x36, 0x00, 0x00, 0x00,
    0x0C, 0x49, 0x44, 0x41, 0x54, 0x28, 0x53, 0x63, 0xF8, 0x0F, 0x00, 0x01,
    0x01, 0x01, 0x00, 0x18, 0xDD, 0x8D, 0xB4, 0x00, 0x00, 0x00, 0x00, 0x49,
    0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
  ]);
  return pngData;
};

// Create icons directory
const iconsDir = path.join(__dirname, '../build/dist/assets/icons');
if (!fs.existsSync(iconsDir)) {
  fs.mkdirSync(iconsDir, { recursive: true });
}

// Create simple icons for development
const sizes = [16, 48, 128];
sizes.forEach(size => {
  const iconData = createSimpleIcon(size);
  fs.writeFileSync(path.join(iconsDir, `icon-${size}.png`), iconData);
  console.log(`Created icon-${size}.png`);
});

console.log('Development icons created successfully!');